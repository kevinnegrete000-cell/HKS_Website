function simulateRelationshipNodeDrop(node,dt,elapsed,inertialForce){
  const data=node?.userData;
  const rest=data?.restPosition;
  const velocity=data?.dropVelocity;
  if(!node||!rest||!velocity)return;

  const supportAttribute=data.support?.geometry?.getAttribute?.('position');
  const updateSupport=()=>{
    if(!supportAttribute)return;
    const array=supportAttribute.array;
    array[0]=0;array[1]=.14;array[2]=0;
    array[3]=rest.x-node.position.x;
    array[4]=rest.y+finiteNumber(data.supportHeight,5)-node.position.y;
    array[5]=rest.z-node.position.z;
    supportAttribute.needsUpdate=true;
  };

  if(prefersReducedMotion){
    node.position.copy(rest);
    velocity.set(0,0,0);
    updateSupport();
    return;
  }

  const delay=finiteNumber(data.dropDelay,0);
  if(elapsed<delay){
    updateSupport();
    return;
  }

  const step=Math.min(Math.max(finiteNumber(dt,0),0),.033);
  const spring=22.5;
  const damping=5.15;
  const forceX=finiteNumber(inertialForce?.x)*.34;
  const forceY=finiteNumber(inertialForce?.y)*.08;
  const forceZ=finiteNumber(inertialForce?.z)*.34;

  velocity.x+=((rest.x-node.position.x)*spring+forceX)*step;
  velocity.y+=((rest.y-node.position.y)*spring+forceY)*step;
  velocity.z+=((rest.z-node.position.z)*spring+forceZ)*step;
  velocity.multiplyScalar(Math.exp(-damping*step));
  node.position.addScaledVector(velocity,step);

  const nearRest=node.position.distanceToSquared(rest)<.000025;
  const nearlyStill=velocity.lengthSq()<.00009;
  const fieldQuiet=(inertialForce?.lengthSq?.()||0)<.0025;
  if(nearRest&&nearlyStill&&fieldQuiet){
    node.position.copy(rest);
    velocity.set(0,0,0);
  }
  updateSupport();
}

function makeHangingConnection(relationship,source,target,index){
  const distance=Math.max(.001,source.distanceTo(target));
  const segmentCount=Math.max(22,Math.min(42,Math.round(distance*2.65)+13));
  const extraLength=.14+(1-relationship.strength)*.07+(index%4)*.008;
  const restLength=distance*(1+extraLength)/segmentCount;
  const direction=target.clone().sub(source);
  const sideways=new THREE.Vector3().crossVectors(direction,new THREE.Vector3(0,1,0));
  if(sideways.lengthSq()<.001)sideways.set(0,0,1);
  sideways.normalize();
  const positions=[];
  const previous=[];
  const entryLift=1.1+distance*.065+(index%3)*.24;
  const sideOffset=((index%2===0)?1:-1)*(.12+(index%5)*.035);

  for(let i=0;i<=segmentCount;i++){
    const t=i/segmentCount;
    const point=source.clone().lerp(target,t);
    if(prefersReducedMotion){
      point.y-=Math.sin(Math.PI*t)*(distance*.17+1.1);
    }else{
      point.y+=Math.sin(Math.PI*t)*entryLift;
      point.addScaledVector(sideways,Math.sin(Math.PI*t)*sideOffset);
    }
    positions.push(point);
    const oldPoint=point.clone();
    if(!prefersReducedMotion&&i>0&&i<segmentCount){
      oldPoint.y+=.16+Math.sin(i*.81+index)*.055;
      oldPoint.addScaledVector(sideways,Math.cos(i*.47+index)*.025);
    }
    previous.push(oldPoint);
  }

  positions[0].copy(source);
  positions[segmentCount].copy(target);
  previous[0].copy(source);
  previous[segmentCount].copy(target);

  const positionArray=new Float32Array((segmentCount+1)*3);
  const geometry=new THREE.BufferGeometry();
  const attribute=new THREE.BufferAttribute(positionArray,3);
  attribute.setUsage(THREE.DynamicDrawUsage);
  geometry.setAttribute('position',attribute);

  const categoryColor=relationshipCategoryColor(relationship.category);
  const haloMaterial=new THREE.LineBasicMaterial({
    color:categoryColor,
    transparent:true,
    opacity:.08,
    depthWrite:false,
    blending:THREE.AdditiveBlending
  });
  const lineMaterial=new THREE.LineBasicMaterial({
    color:categoryColor,
    transparent:true,
    opacity:.34,
    depthWrite:false
  });
  const halo=new THREE.Line(geometry,haloMaterial);
  const line=new THREE.Line(geometry,lineMaterial);
  halo.frustumCulled=false;
  line.frustumCulled=false;

  const beadIndices=[];
  for(let i=2;i<segmentCount;i+=4)beadIndices.push(i);
  const beadMaterial=new THREE.MeshBasicMaterial({color:categoryColor,transparent:true,opacity:.38,depthWrite:false});
  const beads=new THREE.InstancedMesh(new THREE.SphereGeometry(.045,7,6),beadMaterial,beadIndices.length);
  beads.instanceMatrix.setUsage(THREE.DynamicDrawUsage);
  beads.frustumCulled=false;

  const signal=new THREE.Mesh(
    new THREE.SphereGeometry(.075+relationship.strength*.025,10,8),
    new THREE.MeshBasicMaterial({color:categoryColor,transparent:true,opacity:.8,depthWrite:false})
  );
  const label=makeRelationshipLabel(relationship.category,`${Math.round(relationship.strength*100)}% relationship`);
  label.scale.multiplyScalar(.52);
  label.material.opacity=.38;
  label.visible=relationship.strength>=.72;

  return {
    relationship,
    sourcePosition:source,
    targetPosition:target,
    positions,
    previous,
    segmentCount,
    restLength,
    sideways,
    geometry,
    attribute,
    halo,
    line,
    beads,
    beadIndices,
    signal,
    label,
    phase:(index*.137)%1,
    seed:index*1.713,
    direct:false
  };
}

const ropeDelta={x:0,y:0,z:0};
const ropeMatrixHolder={matrix:null};

function finiteNumber(value,fallback=0){
  const number=Number(value);
  return Number.isFinite(number)?number:fallback;
}

function safeConnectionPoint(connection,index){
  const positions=connection?.positions;
  if(!Array.isArray(positions)||positions.length===0)return null;
  const clamped=Math.max(0,Math.min(positions.length-1,Math.floor(finiteNumber(index,0))));
  const point=positions[clamped];
  if(point&&Number.isFinite(point.x)&&Number.isFinite(point.y)&&Number.isFinite(point.z))return point;

  // Repair a missing/corrupt rope point from the nearest valid neighbors.
  let before=null,after=null;
  for(let i=clamped-1;i>=0;i--){
    const candidate=positions[i];
    if(candidate&&Number.isFinite(candidate.x)&&Number.isFinite(candidate.y)&&Number.isFinite(candidate.z)){before=candidate;break;}
  }
  for(let i=clamped+1;i<positions.length;i++){
    const candidate=positions[i];
    if(candidate&&Number.isFinite(candidate.x)&&Number.isFinite(candidate.y)&&Number.isFinite(candidate.z)){after=candidate;break;}
  }
  if(before&&after){
    const repaired=new THREE.Vector3(
      (before.x+after.x)*.5,
      (before.y+after.y)*.5,
      (before.z+after.z)*.5
    );
    positions[clamped]=repaired;
    return repaired;
  }
  const fallback=before||after||connection?.sourcePosition||connection?.targetPosition;
  if(fallback){
    const repaired=new THREE.Vector3(finiteNumber(fallback.x),finiteNumber(fallback.y),finiteNumber(fallback.z));
    positions[clamped]=repaired;
    return repaired;
  }
  return null;
}

function updateHangingConnectionGeometry(connection,elapsed=0){
  if(!connection||!Array.isArray(connection.positions)||connection.positions.length<2)return false;

  const positions=connection.positions;
  const lastAvailable=positions.length-1;
  const declaredSegments=Math.floor(finiteNumber(connection.segmentCount,lastAvailable));
  const segmentCount=Math.max(1,Math.min(declaredSegments,lastAvailable));
  connection.segmentCount=segmentCount;

  const array=connection.attribute?.array;
  if(array){
    for(let index=0;index<positions.length;index++){
      const point=safeConnectionPoint(connection,index);
      const base=index*3;
      array[base]=finiteNumber(point?.x);
      array[base+1]=finiteNumber(point?.y);
      array[base+2]=finiteNumber(point?.z);
    }
    connection.attribute.needsUpdate=true;
  }

  if(!ropeMatrixHolder.matrix)ropeMatrixHolder.matrix=new THREE.Matrix4();
  if(connection.beads&&Array.isArray(connection.beadIndices)){
    connection.beadIndices.forEach((pointIndex,instanceIndex)=>{
      const point=safeConnectionPoint(connection,pointIndex);
      if(!point)return;
      ropeMatrixHolder.matrix.makeTranslation(
        finiteNumber(point.x),finiteNumber(point.y),finiteNumber(point.z)
      );
      connection.beads.setMatrixAt(instanceIndex,ropeMatrixHolder.matrix);
    });
    if(connection.beads.instanceMatrix)connection.beads.instanceMatrix.needsUpdate=true;
  }

  // Intentionally avoid Vector3.copy() here. Direct interpolation cannot receive
  // an undefined source and therefore cannot crash the whole render loop.
  const strength=Math.max(0,Math.min(1,finiteNumber(connection.relationship?.strength,.5)));
  const phase=finiteNumber(connection.phase,0);
  const time=finiteNumber(elapsed,0);
  const signalT=((time*(.045+strength*.035)+phase)%1+1)%1;
  const scaled=Math.max(0,Math.min(segmentCount-.000001,signalT*segmentCount));
  const low=Math.max(0,Math.min(segmentCount-1,Math.floor(scaled)));
  const high=Math.min(segmentCount,low+1);
  const from=safeConnectionPoint(connection,low);
  const to=safeConnectionPoint(connection,high)||from;
  const alpha=Math.max(0,Math.min(1,scaled-low));
  if(connection.signal&&from&&to){
    connection.signal.position.set(
      finiteNumber(from.x)+(finiteNumber(to.x)-finiteNumber(from.x))*alpha,
      finiteNumber(from.y)+(finiteNumber(to.y)-finiteNumber(from.y))*alpha,
      finiteNumber(from.z)+(finiteNumber(to.z)-finiteNumber(from.z))*alpha
    );
  }

  const midpoint=safeConnectionPoint(connection,Math.floor(segmentCount*.5));
  if(connection.label&&midpoint){
    connection.label.position.set(
      finiteNumber(midpoint.x),
      finiteNumber(midpoint.y)+.34,
      finiteNumber(midpoint.z)
    );
  }
  return true;
}

function simulateHangingConnection(connection,dt,gravity,elapsed){
  if(!connection||!Array.isArray(connection.positions)||!Array.isArray(connection.previous))return;
  const points=connection.positions;
  const previous=connection.previous;
  const requestedLast=Math.floor(finiteNumber(connection.segmentCount,points.length-1));
  const last=Math.max(1,Math.min(requestedLast,points.length-1,previous.length-1));
  connection.segmentCount=last;

  // Repair every point before the physics pass so malformed data cannot spread NaNs.
  for(let i=0;i<=last;i++){
    const point=safeConnectionPoint(connection,i);
    if(!point)return;
    if(!previous[i]||!Number.isFinite(previous[i].x)||!Number.isFinite(previous[i].y)||!Number.isFinite(previous[i].z)){
      previous[i]=point.clone();
    }
  }

  if(prefersReducedMotion){
    updateHangingConnectionGeometry(connection,elapsed);
    return;
  }

  const substeps=2;
  const step=Math.min(Math.max(finiteNumber(dt,0),0),.033)/substeps;
  const stepSquared=step*step;
  const damping=.986;
  const gravityX=finiteNumber(gravity?.x);
  const gravityY=finiteNumber(gravity?.y,-7.4);
  const gravityZ=finiteNumber(gravity?.z);
  const sidewaysX=finiteNumber(connection.sideways?.x);
  const sidewaysZ=finiteNumber(connection.sideways?.z);
  const seed=finiteNumber(connection.seed);

  for(let substep=0;substep<substeps;substep++){
    for(let i=1;i<last;i++){
      const point=points[i];
      const old=previous[i];
      const velocityX=(point.x-old.x)*damping;
      const velocityY=(point.y-old.y)*damping;
      const velocityZ=(point.z-old.z)*damping;
      old.set(point.x,point.y,point.z);
      const flutter=Math.sin(finiteNumber(elapsed)*.82+i*.57+seed)*.12;
      point.x+=velocityX+(gravityX+sidewaysX*flutter)*stepSquared;
      point.y+=velocityY+gravityY*stepSquared;
      point.z+=velocityZ+(gravityZ+sidewaysZ*flutter)*stepSquared;
    }

    for(let iteration=0;iteration<6;iteration++){
      const source=connection.sourcePosition;
      const target=connection.targetPosition;
      if(source)points[0].set(finiteNumber(source.x),finiteNumber(source.y),finiteNumber(source.z));
      if(target)points[last].set(finiteNumber(target.x),finiteNumber(target.y),finiteNumber(target.z));

      for(let i=0;i<last;i++){
        const a=points[i];
        const b=points[i+1];
        if(!a||!b)continue;
        ropeDelta.x=b.x-a.x;
        ropeDelta.y=b.y-a.y;
        ropeDelta.z=b.z-a.z;
        const distance=Math.hypot(ropeDelta.x,ropeDelta.y,ropeDelta.z)||.0001;
        const restLength=Math.max(.0001,finiteNumber(connection.restLength,distance));
        const correction=(distance-restLength)/distance;
        if(i===0){
          b.x-=ropeDelta.x*correction;
          b.y-=ropeDelta.y*correction;
          b.z-=ropeDelta.z*correction;
        }else if(i+1===last){
          a.x+=ropeDelta.x*correction;
          a.y+=ropeDelta.y*correction;
          a.z+=ropeDelta.z*correction;
        }else{
          const half=correction*.5;
          a.x+=ropeDelta.x*half;
          a.y+=ropeDelta.y*half;
          a.z+=ropeDelta.z*half;
          b.x-=ropeDelta.x*half;
          b.y-=ropeDelta.y*half;
          b.z-=ropeDelta.z*half;
        }
      }

      for(let i=2;i<last-1;i++){
        const before=points[i-1],point=points[i],after=points[i+1];
        if(!before||!point||!after)continue;
        point.x+=(((before.x+after.x)*.5)-point.x)*.012;
        point.y+=(((before.y+after.y)*.5)-point.y)*.012;
        point.z+=(((before.z+after.z)*.5)-point.z)*.012;
      }
    }
  }
  updateHangingConnectionGeometry(connection,elapsed);
}

