function dominantGeometryAxis(object){
  object.updateMatrixWorld(true);
  const rootInverse=object.matrixWorld.clone().invert();
  const point=new THREE.Vector3();
  const transformed=new THREE.Vector3();
  const samples=[];
  let vertexBudget=0;

  object.traverse(mesh=>{
    if(!mesh.isMesh)return;
    const position=mesh.geometry?.getAttribute?.('position');
    if(!position||position.count===0)return;
    const localMatrix=new THREE.Matrix4().multiplyMatrices(rootInverse,mesh.matrixWorld);
    const stride=Math.max(1,Math.ceil(position.count/5000));
    for(let index=0;index<position.count;index+=stride){
      point.fromBufferAttribute(position,index);
      transformed.copy(point).applyMatrix4(localMatrix);
      if(Number.isFinite(transformed.x)&&Number.isFinite(transformed.y)&&Number.isFinite(transformed.z)){
        samples.push(transformed.clone());
        vertexBudget+=1;
      }
      if(vertexBudget>=18000)break;
    }
  });

  if(samples.length<3)return new THREE.Vector3(0,1,0);
  const center=new THREE.Vector3();
  samples.forEach(sample=>center.add(sample));
  center.multiplyScalar(1/samples.length);

  let xx=0,xy=0,xz=0,yy=0,yz=0,zz=0;
  samples.forEach(sample=>{
    const x=sample.x-center.x;
    const y=sample.y-center.y;
    const z=sample.z-center.z;
    xx+=x*x;xy+=x*y;xz+=x*z;yy+=y*y;yz+=y*z;zz+=z*z;
  });

  // Power iteration finds the covariance matrix's strongest eigenvector.
  let axis=new THREE.Vector3(1,.73,.41).normalize();
  for(let iteration=0;iteration<18;iteration+=1){
    axis.set(
      xx*axis.x+xy*axis.y+xz*axis.z,
      xy*axis.x+yy*axis.y+yz*axis.z,
      xz*axis.x+yz*axis.y+zz*axis.z
    );
    if(axis.lengthSq()<1e-12)return new THREE.Vector3(0,1,0);
    axis.normalize();
  }
  return axis;
}

