function createSpecimenFresnelMaterial(){
  return new THREE.ShaderMaterial({
    uniforms:{
      uOpacity:{value:.58},
      uTime:{value:0},
      uSelected:{value:0}
    },
    vertexShader:`
      varying vec3 vNormal;
      varying vec3 vViewDirection;
      varying vec3 vWorldPosition;
      void main(){
        vec4 worldPosition=modelMatrix*vec4(position,1.0);
        vec4 viewPosition=modelViewMatrix*vec4(position,1.0);
        vNormal=normalize(normalMatrix*normal);
        vViewDirection=normalize(-viewPosition.xyz);
        vWorldPosition=worldPosition.xyz;
        gl_Position=projectionMatrix*viewPosition;
      }
    `,
    fragmentShader:`
      uniform float uOpacity;
      uniform float uTime;
      uniform float uSelected;
      varying vec3 vNormal;
      varying vec3 vViewDirection;
      varying vec3 vWorldPosition;
      void main(){
        float fresnel=pow(1.0-abs(dot(normalize(vNormal),normalize(vViewDirection))),2.15);
        float scan=.5+.5*sin(vWorldPosition.y*8.0-uTime*.7);
        float interior=.025+scan*.012;
        float alpha=(interior+fresnel*(.76+uSelected*.35))*uOpacity;
        vec3 cold=vec3(.56,.65,.69);
        vec3 white=vec3(1.0);
        vec3 color=mix(cold,white,clamp(fresnel*1.5+uSelected*.35,0.0,1.0));
        gl_FragColor=vec4(color,alpha);
      }
    `,
    transparent:true,
    depthWrite:false,
    side:THREE.DoubleSide,
    blending:THREE.AdditiveBlending
  });
}

function createSpecimenFramework(scene){
  const TABLE_Y=-1.38;
  const columns=5;
  const rows=Math.ceil(archiveItems.filter(item=>item.model).length/columns);
  const cellWidth=2.55;
  const cellDepth=2.85;
  const xMin=-(columns*cellWidth)/2;
  const xMax=(columns*cellWidth)/2;
  const zMax=1.62;
  const zMin=zMax-rows*cellDepth;
  const centerZ=(zMin+zMax)/2;

  const tableGroup=new THREE.Group();
  tableGroup.name='specimen-lab-grid';

  const tableSurface=new THREE.Mesh(
    new THREE.PlaneGeometry(xMax-xMin,zMax-zMin),
    new THREE.MeshStandardMaterial({
      color:0x9aa6a7,roughness:.86,metalness:.04,transparent:true,opacity:.038,
      side:THREE.DoubleSide,depthWrite:false
    })
  );
  tableSurface.rotation.x=-Math.PI/2;
  tableSurface.position.set(0,TABLE_Y-.012,centerZ);
  tableSurface.renderOrder=0;
  tableGroup.add(tableSurface);

  const gridMaterial=new THREE.LineBasicMaterial({
    color:0xc3cece,transparent:true,opacity:.19,depthWrite:false
  });
  const gridPoints=[];
  for(let column=0;column<=columns;column+=1){
    const x=xMin+column*cellWidth;
    gridPoints.push(new THREE.Vector3(x,TABLE_Y+.008,zMin),new THREE.Vector3(x,TABLE_Y+.008,zMax));
  }
  for(let row=0;row<=rows;row+=1){
    const z=zMax-row*cellDepth;
    gridPoints.push(new THREE.Vector3(xMin,TABLE_Y+.008,z),new THREE.Vector3(xMax,TABLE_Y+.008,z));
  }
  const grid=new THREE.LineSegments(new THREE.BufferGeometry().setFromPoints(gridPoints),gridMaterial);
  grid.renderOrder=2;
  tableGroup.add(grid);

  const borderMaterial=new THREE.LineBasicMaterial({
    color:0xe1e7e5,transparent:true,opacity:.28,depthWrite:false
  });
  const borderPoints=[
    new THREE.Vector3(xMin,TABLE_Y+.014,zMin),new THREE.Vector3(xMax,TABLE_Y+.014,zMin),
    new THREE.Vector3(xMax,TABLE_Y+.014,zMin),new THREE.Vector3(xMax,TABLE_Y+.014,zMax),
    new THREE.Vector3(xMax,TABLE_Y+.014,zMax),new THREE.Vector3(xMin,TABLE_Y+.014,zMax),
    new THREE.Vector3(xMin,TABLE_Y+.014,zMax),new THREE.Vector3(xMin,TABLE_Y+.014,zMin)
  ];
  tableGroup.add(new THREE.LineSegments(new THREE.BufferGeometry().setFromPoints(borderPoints),borderMaterial));

  const markerMaterial=new THREE.MeshBasicMaterial({
    color:0xe5ecea,transparent:true,opacity:.24,side:THREE.DoubleSide,depthWrite:false
  });
  const crossMaterial=new THREE.LineBasicMaterial({
    color:0xd8e0de,transparent:true,opacity:.22,depthWrite:false
  });
  for(let row=0;row<rows;row+=1){
    for(let column=0;column<columns;column+=1){
      const x=xMin+cellWidth*(column+.5);
      const z=zMax-cellDepth*(row+.5);
      const ring=new THREE.Mesh(new THREE.RingGeometry(.055,.073,28),markerMaterial.clone());
      ring.rotation.x=-Math.PI/2;
      ring.position.set(x,TABLE_Y+.022,z);
      tableGroup.add(ring);
      const crossPoints=[
        new THREE.Vector3(x-.16,TABLE_Y+.02,z),new THREE.Vector3(x+.16,TABLE_Y+.02,z),
        new THREE.Vector3(x,TABLE_Y+.02,z-.16),new THREE.Vector3(x,TABLE_Y+.02,z+.16)
      ];
      tableGroup.add(new THREE.LineSegments(new THREE.BufferGeometry().setFromPoints(crossPoints),crossMaterial.clone()));
    }
  }

  const tableEdge=new THREE.Mesh(
    new THREE.BoxGeometry(xMax-xMin,.11,zMax-zMin),
    new THREE.MeshBasicMaterial({color:0x697374,transparent:true,opacity:.027,depthWrite:false})
  );
  tableEdge.position.set(0,TABLE_Y-.07,centerZ);
  tableGroup.add(tableEdge);

  const roomGrid=new THREE.GridHelper(32,40,0x475052,0x242a2b);
  roomGrid.position.set(0,TABLE_Y-.42,centerZ-1.5);
  roomGrid.material.transparent=true;
  roomGrid.material.opacity=.045;
  roomGrid.material.depthWrite=false;
  tableGroup.add(roomGrid);

  scene.add(tableGroup);
  return tableGroup;
}

function createSpecimenDust(scene){
  const count=1400;
  const positions=new Float32Array(count*3);
  const phases=new Float32Array(count);
  const sizes=new Float32Array(count);
  for(let index=0;index<count;index+=1){
    positions[index*3]=(Math.random()-.5)*22;
    positions[index*3+1]=(Math.random()-.5)*12;
    positions[index*3+2]=-Math.random()*15+3;
    phases[index]=Math.random()*Math.PI*2;
    sizes[index]=.46+Math.random()*.82;
  }
  const geometry=new THREE.BufferGeometry();
  geometry.setAttribute('position',new THREE.BufferAttribute(positions,3));
  geometry.setAttribute('aPhase',new THREE.BufferAttribute(phases,1));
  geometry.setAttribute('aSize',new THREE.BufferAttribute(sizes,1));
  const material=new THREE.ShaderMaterial({
    uniforms:{uTime:{value:0},uOpacity:{value:.31}},
    vertexShader:`
      uniform float uTime;
      attribute float aPhase;
      attribute float aSize;
      varying float vTwinkle;
      void main(){
        vec3 p=position;
        p.x+=sin(uTime*.12+aPhase)*.065;
        p.y+=sin(uTime*.17+aPhase*1.7)*.085;
        p.z+=cos(uTime*.10+aPhase*.8)*.05;
        vec4 mvPosition=modelViewMatrix*vec4(p,1.0);
        gl_PointSize=aSize*(18.0/max(1.0,-mvPosition.z));
        gl_Position=projectionMatrix*mvPosition;
        vTwinkle=.58+.42*sin(uTime*.45+aPhase*2.1);
      }
    `,
    fragmentShader:`
      uniform float uOpacity;
      varying float vTwinkle;
      void main(){
        vec2 centered=gl_PointCoord-vec2(.5);
        float d=length(centered);
        if(d>.5)discard;
        float soft=1.0-smoothstep(.12,.5,d);
        gl_FragColor=vec4(vec3(.88,.93,.94),uOpacity*soft*(.72+.28*vTwinkle));
      }
    `,
    transparent:true,
    depthWrite:false,
    blending:THREE.AdditiveBlending
  });
  const dust=new THREE.Points(geometry,material);
  dust.name='specimen-dust-field';
  scene.add(dust);
  return dust;
}

function specimenLayout(index,scaledHeight=1){
  const columns=5;
  const cellWidth=2.55;
  const cellDepth=2.85;
  const tableY=-1.38;
  const column=index%columns;
  const row=Math.floor(index/columns);
  const x=(column-(columns-1)/2)*cellWidth;
  const z=.195-row*cellDepth;
  const y=tableY+scaledHeight*.5+.2;
  return new THREE.Vector3(x,y,z);
}

async function loadSpecimenObject(item,index,scene,hitMeshes,labelHost){
  const loader=new OBJLoader();
  const object=await new Promise((resolve,reject)=>loader.load(item.model.obj,resolve,undefined,reject));

  // Apply the same per-model upright correction used in the full viewer.
  applyArchiveObjectOrientation(object,item);

  let box=new THREE.Box3().setFromObject(object);
  let center=box.getCenter(new THREE.Vector3());
  let size=box.getSize(new THREE.Vector3());
  const maxDimension=Math.max(size.x,size.y,size.z)||1;
  object.position.sub(center);
  object.updateMatrixWorld(true);

  const material=createSpecimenFresnelMaterial();
  object.traverse(mesh=>{
    if(!mesh.isMesh)return;
    mesh.geometry?.computeVertexNormals?.();
    mesh.material=material;
    mesh.userData.specimenId=item.id;
    hitMeshes.push(mesh);
  });

  const normalized=new THREE.Group();
  const targetSize=1.42;
  const normalizedScale=targetSize/maxDimension;
  normalized.scale.setScalar(normalizedScale);
  normalized.add(object);

  const scaledHeight=Math.max(.35,size.y*normalizedScale);
  const group=new THREE.Group();
  const basePosition=specimenLayout(index,scaledHeight);
  group.position.set(basePosition.x,basePosition.y-1.1,basePosition.z);
  group.scale.setScalar(.001);
  group.rotation.set(0,0,0);
  group.add(normalized);
  group.userData={
    id:item.id,item,index,basePosition,phase:index*.83,
    material,targetOpacity:.60,currentOpacity:0,
    entryDelay:index*.055,targetScale:1,
    baseYaw:0,spinSpeed:(index%2===0?1:-1)*(.055+(index%5)*.006)
  };
  scene.add(group);

  const label=document.createElement('div');
  label.className='specimen-label-item';
  label.dataset.id=item.id;
  label.innerHTML=`<strong>${item.archiveCode}</strong><span>${item.title}</span>`;
  labelHost.appendChild(label);
  group.userData.label=label;
  return group;
}

function createSpecimenRelationshipLines(groupsById,scene){
  return relationships.map(relationship=>{
    const source=groupsById.get(relationship.source);
    const target=groupsById.get(relationship.target);
    if(!source||!target)return null;
    const segmentCount=32;
    const positions=new Float32Array(segmentCount*3);
    const geometry=new THREE.BufferGeometry();
    geometry.setAttribute('position',new THREE.BufferAttribute(positions,3));
    const material=new THREE.LineBasicMaterial({
      color:relationshipCategoryColor(relationship.category),transparent:true,opacity:0,depthWrite:false,
      blending:THREE.AdditiveBlending
    });
    const line=new THREE.Line(geometry,material);
    line.renderOrder=8;
    scene.add(line);
    return {relationship,source,target,line,positions,segmentCount,targetOpacity:0};
  }).filter(Boolean);
}

function updateSpecimenRelationshipLine(entry){
  const source=entry.source.position;
  const target=entry.target.position;
  const middle=source.clone().lerp(target,.5);
  const distance=source.distanceTo(target);
  middle.y+=.55+distance*.12;
  middle.z-=.35+distance*.08;
  const curve=new THREE.QuadraticBezierCurve3(source,middle,target);
  for(let index=0;index<entry.segmentCount;index+=1){
    const point=curve.getPoint(index/(entry.segmentCount-1));
    entry.positions[index*3]=point.x;
    entry.positions[index*3+1]=point.y;
    entry.positions[index*3+2]=point.z;
  }
  entry.line.geometry.attributes.position.needsUpdate=true;
  entry.line.material.opacity+=(entry.targetOpacity-entry.line.material.opacity)*.085;
}

