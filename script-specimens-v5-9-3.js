function renderTimeline(){
  const objectYears=[...new Set(archiveItems.map(item=>item.year))];
  const milestoneYears=timelineMilestones.map(entry=>entry.year);
  const years=[...new Set([...objectYears,...milestoneYears])].sort((a,b)=>b-a);

  $('#timelineList').innerHTML=years.map(year=>{
    const objectCards=archiveItems.filter(item=>item.year===year).map(item=>`
      <article class="timeline-item timeline-object" data-id="${item.id}" tabindex="0">
        <small>${item.archiveCode}</small>
        <h3>${item.title}</h3>
        <p>${item.location} · ${item.material}</p>
        <span class="timeline-item-type">Archive object</span>
      </article>`).join('');

    const milestoneCards=timelineMilestones.filter(entry=>entry.year===year).map(entry=>`
      <article class="timeline-item timeline-milestone${entry.foundation?' timeline-foundation':''}">
        <small>${entry.code}</small>
        <h3>${entry.title}</h3>
        <p>${entry.description}</p>
        <span class="timeline-item-type">${entry.foundation?'Firm foundation':'Historical marker'}</span>
      </article>`).join('');

    return `<section class="year-group${year===1939?' foundation-year':''}">
      <div class="year-label">${year}</div>
      <div class="year-items">${objectCards}${milestoneCards}</div>
    </section>`;
  }).join('');

  $$('.timeline-object').forEach(card=>{
    const open=()=>focusItem(card.dataset.id);
    card.onclick=open;
    card.onkeydown=event=>{
      if(event.key==='Enter'||event.key===' '){event.preventDefault();open();}
    };
  });
}

function openSearch(){
  const overlay=$('#searchOverlay');
  overlay.classList.add('open');
  overlay.setAttribute('aria-hidden','false');
  $('#searchInput').value='';
  renderSearch('');
  setTimeout(()=>$('#searchInput').focus(),30);
}
function closeSearch(){
  const overlay=$('#searchOverlay');
  overlay.classList.remove('open');
  overlay.setAttribute('aria-hidden','true');
}
function renderSearch(query){
  query=query.toLowerCase().trim();
  const results=archiveItems.filter(item=>!query||[item.title,item.legacyArchiveCode,item.legacyCollection,item.sectorCode,item.sectorName,item.cityCode,item.material,item.location,item.typology,item.archiveCode,item.atlas?.city,item.atlas?.country].join(' ').toLowerCase().includes(query)).slice(0,20);
  $('#searchResults').innerHTML=results.map(item=>`<div class="search-result" data-id="${item.id}"><small>${item.archiveCode}</small><strong>${item.title}</strong><span>${item.year} · ${item.location}</span></div>`).join('');
  $$('.search-result').forEach(result=>result.onclick=()=>{
    closeSearch();
    focusItem(result.dataset.id);
  });
}

