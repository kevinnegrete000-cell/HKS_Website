function applyArchiveObjectOrientation(object,item){
  object.position.set(0,0,0);
  object.scale.set(1,1,1);
  object.rotation.set(0,0,0);
  object.quaternion.identity();
  object.updateMatrixWorld(true);

  const manualRotation=MANUAL_MODEL_ROTATIONS[item.id];
  if(manualRotation){
    object.rotation.set(manualRotation[0]||0,manualRotation[1]||0,manualRotation[2]||0);
  }else if(AUTO_UPRIGHT_MODEL_IDS.has(item.id)){
    const dominant=dominantGeometryAxis(object).normalize();
    // A principal axis has no intrinsic direction; choose the upward-facing sign.
    if(dominant.y<0)dominant.negate();
    const upright=new THREE.Quaternion().setFromUnitVectors(
      dominant,
      new THREE.Vector3(0,1,0)
    );
    object.quaternion.copy(upright);
    object.updateMatrixWorld(true);

    // Present the broader side toward the default camera after standing it up.
    const uprightSize=new THREE.Box3().setFromObject(object).getSize(new THREE.Vector3());
    if(uprightSize.z>uprightSize.x*1.08){
      object.rotateY(Math.PI/2);
    }
  }else{
    const [rx,ry,rz]=item.model.rotation||[0,0,0];
    object.rotation.set(rx||0,ry||0,rz||0);
  }
  object.updateMatrixWorld(true);
}

