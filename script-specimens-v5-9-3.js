function collectAtlasGeoJsonRings(input,rings=[]){
  if(!input)return rings;
  if(input.type==='FeatureCollection'){
    input.features?.forEach(feature=>collectAtlasGeoJsonRings(feature,rings));
    return rings;
  }
  if(input.type==='Feature')return collectAtlasGeoJsonRings(input.geometry,rings);
  if(input.type==='GeometryCollection'){
    input.geometries?.forEach(geometry=>collectAtlasGeoJsonRings(geometry,rings));
    return rings;
  }
  if(input.type==='Polygon'){
    input.coordinates?.forEach(ring=>rings.push(ring));
    return rings;
  }
  if(input.type==='MultiPolygon'){
    input.coordinates?.forEach(polygon=>polygon.forEach(ring=>rings.push(ring)));
  }
  return rings;
}

function splitAtlasRingAtDateline(ring){
  const chunks=[];
  let current=[];
  let previousLon=null;
  ring.forEach(coordinate=>{
    const lon=Number(coordinate?.[0]);
    const lat=Number(coordinate?.[1]);
    if(!Number.isFinite(lon)||!Number.isFinite(lat))return;
    if(previousLon!==null&&Math.abs(lon-previousLon)>180){
      if(current.length>1)chunks.push(current);
      current=[];
    }
    current.push([lon,lat]);
    previousLon=lon;
  });
  if(current.length>1)chunks.push(current);
  return chunks;
}

async function addAccurateAtlasCoastlines(globeGroup,radius,material){
  try{
    const response=await fetch('./assets/data/world-land-ne110.geojson',{cache:'force-cache'});
    if(!response.ok)throw new Error(`Coastline request failed (${response.status})`);
    const geojson=await response.json();
    const rings=collectAtlasGeoJsonRings(geojson);
    if(!rings.length)throw new Error('Coastline dataset contained no polygon rings');

    const coastlineGroup=new THREE.Group();
    coastlineGroup.name='natural-earth-coastlines';
    rings.forEach(ring=>{
      splitAtlasRingAtDateline(ring).forEach(chunk=>{
        if(chunk.length<2)return;
        const points=chunk.map(([lon,lat])=>latLonToVector3(lat,lon,radius+.026));
        coastlineGroup.add(makeAtlasLine(points,material,false));
      });
    });
    globeGroup.add(coastlineGroup);
    return true;
  }catch(error){
    console.warn('[HKS] Accurate Atlas coastlines could not load; using abstract fallback.',error);
    return false;
  }
}

function createAtlasPointMaterial({size=1.15,opacity=.7}={}){
  return new THREE.ShaderMaterial({
    transparent:true,
    depthWrite:false,
    depthTest:true,
    blending:THREE.NormalBlending,
    uniforms:{
      uTime:{value:0},
      uSize:{value:size},
      uOpacity:{value:opacity}
    },
    vertexShader:`
      attribute float aPhase;
      uniform float uTime;
      uniform float uSize;
      varying float vPulse;
      void main(){
        vec4 mvPosition=modelViewMatrix*vec4(position,1.0);
        float pulse=.94+.06*sin(uTime*.52+aPhase);
        vPulse=pulse;
        gl_PointSize=max(.55,uSize*pulse*(7.0/max(1.0,-mvPosition.z)));
        gl_Position=projectionMatrix*mvPosition;
      }
    `,
    fragmentShader:`
      uniform float uOpacity;
      varying float vPulse;
      void main(){
        vec2 centered=gl_PointCoord-vec2(.5);
        float distanceFromCenter=length(centered);
        if(distanceFromCenter>.5)discard;
        float alpha=1.0-smoothstep(.20,.5,distanceFromCenter);
        gl_FragColor=vec4(vec3(1.0),uOpacity*alpha*(.94+.06*vPulse));
      }
    `
  });
}

function seededAtlasRandom(seed=1){
  let value=seed>>>0;
  return ()=>{
    value=(value*1664525+1013904223)>>>0;
    return value/4294967296;
  };
}

function buildAtlasPointGeometry(sourceGeometry,extraSamples=0,seed=1,includeVertices=false){
  const geometry=sourceGeometry?.clone?.();
  const position=geometry?.getAttribute?.('position');
  if(!position)return new THREE.BufferGeometry();
  const index=geometry.index;
  const random=seededAtlasRandom(seed);
  const pointValues=[];
  const phases=[];

  // Sampling the original mesh vertices exposed triangulation seams and
  // sculpting creases. Uniform triangle samples create a clean point field.
  if(includeVertices){
    for(let i=0;i<position.count;i+=1){
      pointValues.push(position.getX(i),position.getY(i),position.getZ(i));
      phases.push(random()*Math.PI*2);
    }
  }

  const triangleCount=index?Math.floor(index.count/3):Math.floor(position.count/3);
  const a=new THREE.Vector3(),b=new THREE.Vector3(),c=new THREE.Vector3(),sample=new THREE.Vector3();
  for(let sampleIndex=0;sampleIndex<extraSamples&&triangleCount>0;sampleIndex+=1){
    const triangle=Math.floor(random()*triangleCount);
    const ia=index?index.getX(triangle*3):triangle*3;
    const ib=index?index.getX(triangle*3+1):triangle*3+1;
    const ic=index?index.getX(triangle*3+2):triangle*3+2;
    a.fromBufferAttribute(position,ia);
    b.fromBufferAttribute(position,ib);
    c.fromBufferAttribute(position,ic);
    let r1=Math.sqrt(random());
    let r2=random();
    sample.copy(a).multiplyScalar(1-r1)
      .addScaledVector(b,r1*(1-r2))
      .addScaledVector(c,r1*r2);
    pointValues.push(sample.x,sample.y,sample.z);
    phases.push(random()*Math.PI*2);
  }

  const pointGeometry=new THREE.BufferGeometry();
  pointGeometry.setAttribute('position',new THREE.Float32BufferAttribute(pointValues,3));
  pointGeometry.setAttribute('aPhase',new THREE.Float32BufferAttribute(phases,1));
  geometry.dispose?.();
  return pointGeometry;
}

async function loadAtlasEarthModel(globeGroup,radius){
  try{
    const loader=new OBJLoader();
    const earth=await new Promise((resolve,reject)=>loader.load(
      './assets/models/earth/Mundo.obj',
      resolve,
      undefined,
      reject
    ));

    const earthMeshes=[];
    const pointMaterials=[];
    const surfaceMaterials=[];
    earth.traverse(object=>{
      if(!object.isMesh)return;
      earthMeshes.push(object);
      const sourceMaterialNames=(Array.isArray(object.material)?object.material:[object.material])
        .map(material=>material?.name||'')
        .join(' ');
      const objectName=`${object.name||''} ${object.parent?.name||''} ${sourceMaterialNames}`.toLowerCase();
      const isOcean=objectName.includes('mundo_azul')||objectName.includes('azul')||objectName.includes('blinn3sg');
      object.geometry?.computeVertexNormals?.();

      // Keep a very light, smooth surface under the point cloud. No wireframe,
      // no triangle edges, and no flat shading—only a soft continuous shell.
      const surfaceMaterial=new THREE.MeshStandardMaterial({
        color:isOcean?0x76807f:0xdce1df,
        roughness:1,
        metalness:0,
        transparent:true,
        opacity:isOcean?.052:.075,
        depthWrite:false,
        side:THREE.DoubleSide,
        flatShading:false
      });
      surfaceMaterials.push(surfaceMaterial);
      object.material=surfaceMaterial;
      object.renderOrder=0;
      object.castShadow=false;
      object.receiveShadow=false;

      // Use a much sparser cloud so the continental silhouettes read clearly.
      // Land still carries more density than ocean, but both are reduced for legibility.
      const pointGeometry=buildAtlasPointGeometry(
        object.geometry,
        isOcean?3500:36000,
        isOcean?104729:83717,
        false
      );
      const pointMaterial=createAtlasPointMaterial({
        size:isOcean?.34:.62,
        opacity:isOcean?.08:.68
      });
      pointMaterials.push(pointMaterial);
      const cloud=new THREE.Points(pointGeometry,pointMaterial);
      cloud.name=isOcean?'atlas-ocean-point-cloud':'atlas-land-point-cloud';
      cloud.renderOrder=isOcean?2:3;
      object.add(cloud);
    });

    const bounds=new THREE.Box3().setFromObject(earth);
    const center=bounds.getCenter(new THREE.Vector3());
    const sphere=bounds.getBoundingSphere(new THREE.Sphere());
    if(!Number.isFinite(sphere.radius)||sphere.radius<=0)throw new Error('Earth OBJ has invalid bounds');
    earth.position.copy(center).multiplyScalar(-1);

    const earthRoot=new THREE.Group();
    earthRoot.name='mundo-refined-point-cloud-earth-root';
    earthRoot.scale.setScalar(radius/sphere.radius);
    earthRoot.add(earth);

    // A nearly invisible inner core improves depth without becoming a black orb.
    const depthCore=new THREE.Mesh(
      new THREE.SphereGeometry(radius*.978,64,42),
      new THREE.MeshBasicMaterial({
        color:0x0b1110,
        transparent:true,
        opacity:.018,
        depthWrite:false,
        side:THREE.FrontSide
      })
    );
    depthCore.name='atlas-point-cloud-depth-core';
    globeGroup.add(depthCore);
    globeGroup.add(earthRoot);
    globeGroup.updateMatrixWorld(true);
    return {root:earthRoot,surfaceMeshes:earthMeshes,pointMaterials,surfaceMaterials,depthCore};
  }catch(error){
    console.error('[HKS] Mundo.obj could not load; using point-cloud sphere fallback.',error);
    const fallbackGeometry=new THREE.SphereGeometry(radius,128,80);
    const fallbackMaterial=createAtlasPointMaterial({size:.72,opacity:.52});
    const fallback=new THREE.Points(fallbackGeometry,fallbackMaterial);
    fallback.name='atlas-point-cloud-earth-fallback';
    globeGroup.add(fallback);
    return {root:fallback,surfaceMeshes:[],pointMaterials:[fallbackMaterial],surfaceMaterials:[],depthCore:null};
  }
}

function projectAtlasPointToEarthSurface(direction,globeGroup,earthData,fallbackRadius,offset=.018){
  const unit=direction.clone().normalize();
  const meshes=earthData?.surfaceMeshes||[];
  if(meshes.length){
    globeGroup.updateMatrixWorld(true);
    const originLocal=unit.clone().multiplyScalar(fallbackRadius*3.2);
    const centerLocal=new THREE.Vector3();
    const originWorld=globeGroup.localToWorld(originLocal.clone());
    const centerWorld=globeGroup.localToWorld(centerLocal.clone());
    const raycaster=new THREE.Raycaster(originWorld,centerWorld.sub(originWorld).normalize(),0,fallbackRadius*8);
    const hit=raycaster.intersectObjects(meshes,true)[0];
    if(hit?.point){
      const localPoint=globeGroup.worldToLocal(hit.point.clone());
      return localPoint.add(localPoint.clone().normalize().multiplyScalar(offset));
    }
  }
  return unit.multiplyScalar(fallbackRadius+offset);
}

function makeAtlasArc(sourceItem,targetItem,relationship,radius,index,surfacePoints){
  const source=(surfacePoints?.get(sourceItem.id)||latLonToVector3(sourceItem.atlas.lat,sourceItem.atlas.lon,radius)).clone();
  const target=(surfacePoints?.get(targetItem.id)||latLonToVector3(targetItem.atlas.lat,targetItem.atlas.lon,radius)).clone();
  const sourceDirection=source.clone().normalize();
  const targetDirection=target.clone().normalize();
  const angle=sourceDirection.angleTo(targetDirection);
  const axis=new THREE.Vector3().crossVectors(sourceDirection,targetDirection);
  const canRotate=axis.lengthSq()>1e-8;
  if(canRotate)axis.normalize();
  const height=.055+angle*.13+relationship.strength*.045;
  const points=[];
  for(let step=0;step<=72;step+=1){
    const t=step/72;
    const direction=canRotate
      ? sourceDirection.clone().applyAxisAngle(axis,angle*t)
      : sourceDirection.clone().lerp(targetDirection,t).normalize();
    const surfaceRadius=THREE.MathUtils.lerp(source.length(),target.length(),t);
    direction.multiplyScalar(surfaceRadius+.014+Math.sin(Math.PI*t)*height);
    points.push(direction);
  }
  const color=relationshipCategoryColor(relationship.category);
  const material=new THREE.LineBasicMaterial({color,transparent:true,opacity:.2,depthWrite:false});
  const line=makeAtlasLine(points,material,false);
  line.userData={relationship,source:relationship.source,target:relationship.target,baseOpacity:.2};
  const signal=new THREE.Mesh(
    new THREE.SphereGeometry(.018,10,8),
    new THREE.MeshBasicMaterial({color,transparent:true,opacity:.78,depthWrite:false})
  );
  signal.userData.offset=(index*.173)%1;
  signal.position.copy(points[0]);
  return {line,signal,points,relationship};
}

