  lineMerge.appendChild(svgEl('feMergeNode',{in:'SourceGraphic'}));
  lineGlow.appendChild(lineMerge);
  const pointGlow=svgEl('filter',{id:'pointGlow',x:'-100%',y:'-100%',width:'300%',height:'300%'});
