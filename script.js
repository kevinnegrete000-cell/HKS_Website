  destroyModelViewer();
  const panel=$('#detailPanel');
  panel.classList.remove('open','model-mode');
  panel.setAttribute('aria-hidden','true');
  state.followSelected=false;
  state.orbitPaused=false;
  state.selected=null;
  $$('.node').forEach(node=>node.classList.remove('selected'));
